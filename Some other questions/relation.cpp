#include<iostream>
using namespace std;
int main(){
    int p=10,q=15;
    // ==operator
    cout<<(p==q)<<endl;//0
    // !=operator
    cout<<(p!=q)<<endl;//1
    // < operator
    cout<<(p<q)<<endl;//1
    // > operator
    cout<<(p>q)<<endl;//0
    // <= operator
    cout<<(p<=q)<<endl;//1
    // >= operator
    cout<<(p>=q)<<endl;//0
    return 0;

}